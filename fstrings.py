variable = "world!"

#This is how you would normally concatenate strings
print("Hello",variable)
#This is how you would concatenate strings using formatted strings
print(f"Hello {variable}")
#You can also perform calculations and still concatenate integers without needing to convert to strings first
print(f"3 + 3 = {3+3}")
#You can also multiply strings
print(f"Hello {variable*3}")